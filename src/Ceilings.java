public class Ceilings {
    private int height;
    private int paintedColor;

    public Ceilings(int height, int paintedColor){
        this.height = height;
        this.paintedColor = paintedColor;
    }

    public int getHeight(){
        return this.height;
    }

    public int getPaintedColor(){
        return this.paintedColor;
    }
}
